public class UserInfo {

    private String email;
    private String name;
    private String birth;
    private String wantJob;
    private String edu;
    private String address;
    private String phone;
    private String etccareer;
    private String certificate;
    private String specialNote;

    public UserInfo() {
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }


    public String getEdu() {
        return edu;
    }

    public void setEdu(String edu) {
        this.edu = edu;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getEtccareer() {
        return etccareer;
    }

    public void setEtccareer(String etccareer) {
        this.etccareer = etccareer;
    }

    public String getCertificate() {
        return certificate;
    }

    public void setCertificate(String certificate) {
        this.certificate = certificate;
    }

    public String getSpecialNote() {
        return specialNote;
    }

    public void setSpecialNote(String specialNote) {
        this.specialNote = specialNote;
    }

    public String getBirth() {
        return birth;
    }

    public void setBirth(String birth) {
        this.birth = birth;
    }


    public String getWantJob() {
        return wantJob;
    }

    public void setWantJob(String wantJob) {
        this.wantJob = wantJob;
    }
}
